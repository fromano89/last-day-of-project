#include <unistd.h>

void prova_alphabet(char c)
	{
	write(1, &c, 1);
	}
int main()
{ 
	int i;
  		
	i = 97;
	while (i <= 122)
	{
		prova_alphabet(i);
		i++;
	}
}

		

