int	ft_atoi(char *str)
{
	int	i;
	int	n;
	int	m;

	m = 1;
	i = 0;
	n = 0;
	while ((str[i] == '\t') || (str[i] == '\n') || (str[i] == '\v')
		|| (str[i] == '\f') || (str[i] == '\r') || (str[i] == ' '))
		i++;
	while ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			m *= -1;
		i++;
	}
	while ((str[i] >= '0') && (str[i] <= '9') && (str[i] != 0))
	{
		n = (n * 10) + (str[i] - 48);
		i++;
	}
	return (n * m);
}
