#include <unistd.h>

void	stampa(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		stampa(str[i]);
		i++;
	}
}
