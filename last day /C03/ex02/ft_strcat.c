char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (dest[n] != '\0')
		n++;
	while (src[i] != '\0')
	{	
			dest[n + i] = src[i];
			i++;
	}	
	dest[n + i] = '\0';
	return (dest);
}
